package com.melonsail.app.melonfriends.sns.melon;

public class FeedTags {

	private String sID;
	private String sSNS;
	private String sFeed_ID;
	private String sName;
	private String sOffset;
	private String sLength;
	private String sType;
	public void setsID(String sID) {
		this.sID = sID;
	}
	public String getsID() {
		return sID;
	}
	public void setsSNS(String sSNS) {
		this.sSNS = sSNS;
	}
	public String getsSNS() {
		return sSNS;
	}
	public void setsFeed_ID(String sFeed_ID) {
		this.sFeed_ID = sFeed_ID;
	}
	public String getsFeed_ID() {
		return sFeed_ID;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public String getsName() {
		return sName;
	}
	public void setsOffset(String sOffset) {
		this.sOffset = sOffset;
	}
	public String getsOffset() {
		return sOffset;
	}
	public void setsLength(String sLength) {
		this.sLength = sLength;
	}
	public String getsLength() {
		return sLength;
	}
	public void setsType(String sType) {
		this.sType = sType;
	}
	public String getsType() {
		return sType;
	}
}
